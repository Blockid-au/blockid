// Enhanced email delivery with DOCX + PDF attachments.
//
// Sends styled HTML emails for SVI reports with optional PDF/DOCX attachments.
// Uses the same SMTP infrastructure as email.ts (Nodemailer → Resend fallback).
// Supports English and Vietnamese locales.

import "server-only";
import { sendEmail } from "./email";
import {
  ensureEmailPreferences,
  canSendEmail,
  getUnsubscribeUrl,
  getPreferencesUrl,
} from "./email-preferences";

// ── Types ──────────────────────────────────────────────────────────────

export interface EnhancedReportEmailOptions {
  to: string;
  startupName: string;
  sviScore: number;
  stageLabel: string;
  reportSummary: string;
  pdfBuffer?: Buffer;
  docxBuffer?: Buffer;
  reportUrl: string;
  locale?: "en" | "vi";
}

// ── Helpers ────────────────────────────────────────────────────────────

function siteUrl(): string {
  return (process.env.NEXT_PUBLIC_SITE_URL || "https://blockid.au").replace(
    /\/$/,
    "",
  );
}

/** Return badge color based on SVI score tier. */
function scoreColor(score: number): string {
  if (score >= 80) return "#10b981"; // green — strong
  if (score >= 60) return "#6c5ce7"; // purple — promising
  if (score >= 40) return "#f59e0b"; // amber — developing
  return "#ef4444";                  // red — early
}

/** Return tier label based on SVI score. */
function scoreTier(score: number, locale: "en" | "vi"): string {
  if (locale === "vi") {
    if (score >= 80) return "Mạnh";
    if (score >= 60) return "Tiềm năng";
    if (score >= 40) return "Đang phát triển";
    return "Giai đoạn đầu";
  }
  if (score >= 80) return "Strong";
  if (score >= 60) return "Promising";
  if (score >= 40) return "Developing";
  return "Early Stage";
}

