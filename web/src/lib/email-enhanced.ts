  const t = LOCALE[locale];
  const color = scoreColor(opts.sviScore);
  const tier = scoreTier(opts.sviScore, locale);
  const summary = opts.reportSummary.slice(0, 500);

  const attachmentsList: string[] = [];
  if (opts.pdfBuffer) attachmentsList.push(`BlockID-SVI-${opts.startupName}.pdf`);
  if (opts.docxBuffer) attachmentsList.push(`BlockID-SVI-${opts.startupName}.docx`);

