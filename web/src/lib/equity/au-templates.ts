// 2. Vesting Agreement (4-yr / 1-yr cliff with leaver provisions)
// ---------------------------------------------------------------------------

export function vestingAgreement(
  startup: StartupCtx,
  member: MemberCtx,
  vesting: VestingCtx,
): string {
  return `# Vesting Agreement

**Between:** ${startup.startupName} (ACN [insert]) ("Company")
**And:** ${member.name} ("Holder")

**Effective date:** ${fmtDate(vesting.startDate)}

## 1. Subject shares

${fmtInt(vesting.totalShares)} ordinary shares (or option-equivalents) in the
Company held by the Holder ("Subject Shares").

## 2. Vesting schedule

- **Cliff:** ${vesting.cliffMonths} months.
- **Vesting period:** ${vesting.vestMonths} months from ${fmtDate(vesting.startDate)}.
- **Schedule type:** ${vesting.scheduleType}.

Until vested, the Subject Shares are subject to compulsory buy-back at the
lower of issue price and fair market value.

## 3. Acceleration

${vesting.accelerateOnExit
  ? "On a Change of Control (sale of >50% of voting shares, sale of business, listing), all unvested Subject Shares vest in full immediately prior to completion (single-trigger acceleration)."
  : "No automatic acceleration. The board may, in its sole discretion, accelerate vesting on a Change of Control."}

## 4. Leaver provisions

- **Good leaver** (death, permanent disability, redundancy, mutual separation):
  vested shares retained; unvested shares bought back at fair market value.
- **Bad leaver** (resignation without good reason within the vesting period,
  summary dismissal for cause, material breach): all unvested shares bought
  back at A$0.001 each; vested shares bought back at the lower of issue price
  and fair market value.

## 5. Restraints (AU law)

The Holder agrees not to, while holding Subject Shares and for 12 months after
ceasing to hold them, solicit Company customers or employees within Australia.
This restraint is intended to be reasonable and severable under the *Restraints
of Trade Act 1976* (NSW) and equivalent state law; if held unreasonable, it
applies for the shorter period / smaller area that is reasonable.

## 6. Governing law

This agreement is governed by the laws of New South Wales, Australia.

---

Signed by Company director: ____________________ Date: ____________

Signed by Holder (${member.name}): ____________________ Date: ____________

> ${DISCLAIMER}
`;
}

