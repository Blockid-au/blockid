// 4. Cap Table Summary / Shareholder Register
// ---------------------------------------------------------------------------

export function capTableSummary(
  startup: StartupCtx,
  cap: CapTableCtx,
): string {
  const lines: string[] = [];
  lines.push(`# Shareholder Register & Cap Table Summary`);
  lines.push("");
  lines.push(`**${startup.startupName}** (ACN [insert])`);
  lines.push(`**As at:** ${fmtDate(new Date().toISOString().slice(0, 10))}`);
  lines.push(`**Jurisdiction:** ${startup.jurisdiction ?? "AU"}`);
  if (startup.preMoneyValuationAud) {
    lines.push(`**Implied pre-money valuation:** ${fmtAud(startup.preMoneyValuationAud)}`);
  }
  lines.push(`**Total issued shares:** ${fmtInt(cap.totalShares)}`);
  if (cap.esopPoolPct != null) {
    lines.push(`**ESOP pool:** ${cap.esopPoolPct.toFixed(2)}%`);
  }
  lines.push("");
  lines.push("## Register of members (Corporations Act 2001 s169)");
  lines.push("");
  lines.push("| Name | Role | Share class | Shares issued | Options | Ownership % | Fully-diluted % | Value (AUD) |");
  lines.push("|------|------|-------------|--------------:|--------:|------------:|----------------:|------------:|");
  for (const r of cap.rows) {
    lines.push(
      `| ${r.name} | ${r.role} | ${r.shareClass} | ${fmtInt(r.shares)} | ${fmtInt(r.options)} | ${r.ownershipPct.toFixed(2)}% | ${r.fullyDilutedPct.toFixed(2)}% | ${fmtAud(r.valueAud)} |`,
    );
  }
  const totalShares = cap.rows.reduce((s, r) => s + r.shares, 0);
  const totalOptions = cap.rows.reduce((s, r) => s + r.options, 0);
  lines.push("");
  lines.push(`**Totals:** ${fmtInt(totalShares)} shares, ${fmtInt(totalOptions)} options, issued capital ${fmtAud(totalShares * ((startup.preMoneyValuationAud ?? 0) / cap.totalShares))}.`);
  lines.push("");
  lines.push("This register satisfies the form requirements of section 169 of the");
  lines.push("*Corporations Act 2001* (Cth) and must be updated within 28 days of any change.");
  lines.push("");
  lines.push(`> ${DISCLAIMER}`);
  return lines.join("\n");
}

