// T0097 — AU-native equity document templates.
//
// All templates are AU-law-compliant boilerplate (general info, not legal
// advice — the rendered doc carries that disclaimer). They are designed to
// fill cleanly from the T0094 equity_plans / equity_members / esop_pools /
// equity_vesting_schedules schema.

export interface StartupCtx {
  startupName: string;
  incorporationDate?: string | null;
  jurisdiction?: string; // default "AU"
  totalShares: number;
  preMoneyValuationAud?: number | null;
}

export interface MemberCtx {
  name: string;
  email?: string | null;
  role: string;
  sharesIssued?: number;
  optionsGranted?: number;
  joinDate: string;
}

export interface VestingCtx {
  totalShares: number;
  cliffMonths: number;
  vestMonths: number;
  scheduleType: string;
  startDate: string;
  accelerateOnExit?: boolean;
}

export interface CapTableCtx {
  rows: Array<{
    name: string;
    role: string;
    shareClass: string;
    shares: number;
    options: number;
    ownershipPct: number;
    fullyDilutedPct: number;
    valueAud: number;
  }>;
  totalShares: number;
  esopPoolPct?: number;
  preMoneyValuationAud?: number | null;
}

const DISCLAIMER =
  "This document is generated by BlockID.au as general information only. " +
  "It is not legal, tax, or financial advice. Before relying on it, obtain " +
  "advice from a qualified Australian solicitor and a registered tax agent.";

function fmtDate(iso?: string | null): string {
  if (!iso) return "[date]";
  const d = new Date(`${iso}T00:00:00Z`);
  if (Number.isNaN(d.getTime())) return iso;
  return d.toLocaleDateString("en-AU", { day: "2-digit", month: "long", year: "numeric" });
}

function fmtAud(n?: number | null): string {
  if (n == null || Number.isNaN(n)) return "A$—";
  return new Intl.NumberFormat("en-AU", {
    style: "currency",
    currency: "AUD",
    maximumFractionDigits: 0,
  }).format(n);
}

function fmtInt(n: number): string {
  return new Intl.NumberFormat("en-AU").format(Math.round(n));
}

// ---------------------------------------------------------------------------
// 1. ESOP Grant Letter (AU ESS / Division 83A ITAA 1997 compliant)
// ---------------------------------------------------------------------------

export function esopGrantLetter(
  startup: StartupCtx,
  member: MemberCtx,
  vesting: VestingCtx,
  exercisePriceAud: number,
): string {
  return `# Employee Share Scheme — Option Grant Letter

**${startup.startupName}** (ACN [insert]) — Issued ${fmtDate(new Date().toISOString().slice(0, 10))}

---

**Grantee:** ${member.name}${member.email ? `  \n**Email:** ${member.email}` : ""}
**Role:** ${member.role}
**Grant date:** ${fmtDate(member.joinDate)}

## 1. Grant

The Company grants you **${fmtInt(vesting.totalShares)} options** to subscribe for
ordinary shares in ${startup.startupName} at an exercise price of **${fmtAud(exercisePriceAud)} per share**.

This grant is made under the Company's Employee Share Scheme ("ESS"), which has
been adopted by the board and is intended to qualify as an ESS interest under
Division 83A of the *Income Tax Assessment Act 1997* (Cth).

## 2. Vesting

- **Cliff:** ${vesting.cliffMonths} months from ${fmtDate(vesting.startDate)}.
- **Total vesting period:** ${vesting.vestMonths} months.
- **Schedule:** ${vesting.scheduleType}.
- **Acceleration on exit:** ${vesting.accelerateOnExit ? "Yes — full acceleration on a Change of Control or qualifying sale." : "No."}

No options vest before the cliff date. On the cliff date, options earned up to
that date vest in a single tranche; the remainder vest in equal periods until
fully vested.

## 3. Exercise

Vested options may be exercised at any time prior to expiry (10 years from
grant) by delivering an exercise notice and payment of the exercise price to
the Company.

## 4. AU tax treatment

This ESS is intended to qualify for the **startup tax concession** in
section 83A-33 of the ITAA 1997. Where eligible (Company is an unlisted
Australian-resident company, less than 10 years old, aggregated turnover under
A$50M, etc.), the discount is *not* assessed at grant and CGT discount rules
apply on disposal.

The Company will issue an ESS Statement to you for each income year in which
a taxing event occurs and lodge an ESS Annual Report with the ATO.

## 5. Leaver provisions

- **Good leaver** (death, permanent disability, redundancy, retirement at NPA):
  vested options remain exercisable; unvested options lapse.
- **Bad leaver** (resignation < 12 months, summary dismissal for cause): all
  options (vested and unvested) lapse.

## 6. ASIC

The Company will rely on the ESS disclosure relief under section 1100W of the
*Corporations Act 2001* (Cth) and ASIC Class Order [CO 14/1000] /
ASIC Corporations (Employee Share Schemes) Instrument 2022/1021 where
applicable.

---

Signed for and on behalf of **${startup.startupName}**:

Director: ____________________ Date: ____________

Accepted by Grantee:

${member.name}: ____________________ Date: ____________

---

> ${DISCLAIMER}
`;
}

