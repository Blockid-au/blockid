// 3. Founders Agreement (IP assignment + vesting + restraint)
// ---------------------------------------------------------------------------

export function foundersAgreement(
  startup: StartupCtx,
  founders: MemberCtx[],
): string {
  const founderList = founders
    .map(
      (f) =>
        `- **${f.name}**${f.email ? ` (${f.email})` : ""} — ${f.role}, joined ${fmtDate(f.joinDate)}` +
        (f.sharesIssued ? `, holding ${fmtInt(f.sharesIssued)} ordinary shares` : ""),
    )
    .join("\n");

  return `# Founders Agreement

**Company:** ${startup.startupName} (ACN [insert])
**Incorporated:** ${fmtDate(startup.incorporationDate)}
**Jurisdiction:** ${startup.jurisdiction ?? "AU"}

## 1. Founders

${founderList || "- [No founders recorded]"}

## 2. Roles and time commitment

Each Founder commits to working full-time on the Company's business and not
engaging in any competing activity. Material changes to role, title or time
commitment require unanimous Founder consent.

## 3. Equity and vesting

Founder shares vest over 4 years with a 12-month cliff from each Founder's
start date, in accordance with each Founder's individual Vesting Agreement.
Unvested shares are subject to buy-back as set out in those agreements.

## 4. Intellectual property assignment

Each Founder hereby irrevocably assigns to the Company all right, title and
interest in any intellectual property created by them, alone or with others,
that relates to the Company's business — whether created before, on or after
the date of this agreement. Each Founder agrees to execute such further
documents and do such further acts as the Company may reasonably require to
perfect this assignment.

This assignment is intended to cover copyright, patents, designs, trade marks,
trade secrets, confidential information, and any moral rights consent required
under the *Copyright Act 1968* (Cth) Part IX.

## 5. Decision making

Day-to-day decisions are made by the CEO. Decisions outside the ordinary
course (raising capital, issuing shares, hiring senior staff, changing the
business model, M&A) require approval of a majority of the Founders.

## 6. Restraint of trade

Each Founder agrees that while they hold shares in the Company and for 12
months after ceasing to do so, they will not:

(a) carry on or be involved in any business that competes with the Company in
Australia or any other jurisdiction in which the Company carries on business;

(b) solicit any customer, supplier or employee of the Company.

If a court finds this restraint unreasonable, the parties intend the restraint
to apply for the shorter period / smaller area that is reasonable
(cascading restraint, valid under *Restraints of Trade Act 1976* (NSW)).

## 7. Confidentiality

Each Founder must keep the Company's confidential information confidential
during and after their involvement with the Company.

## 8. Dispute resolution

The parties will attempt to resolve any dispute in good faith. Failing
resolution within 30 days, the dispute is referred to mediation under the
Resolution Institute Mediation Rules before litigation in the courts of NSW.

## 9. Governing law

This agreement is governed by the laws of New South Wales, Australia.

---

Signed by each Founder:

${founders.map((f) => `${f.name}: ____________________ Date: ____________`).join("\n\n")}

> ${DISCLAIMER}
`;
}

